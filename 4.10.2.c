#include<stdio.h>
int main()
{
    float I;
    printf("请输入当月利润（元）：");
    scanf("%f",&I);
    printf("应发奖金为（元）：");
    if(I<=100000)printf("%.2f",I*0.1);
    else if((100000<I)&&(I<=200000))printf("%.2f",100000*0.1+(I-100000)*0.075);
    else if((200000<I)&&(I<=400000))printf("%.2f",100000*0.1+100000*0.075+(I-200000)*0.05);
    else if((400000<I)&&(I<=600000))printf("%.2f",100000*0.1+100000*0.075+200000*0.05+(I-400000)*0.03);
    else if((600000<I)&&(I<=1000000))printf("%.2f",100000*0.1+100000*0.075+200000*0.05+200000*0.03+(I-600000)*0.015);
    else printf("%.2f",100000*0.1+100000*0.075+200000*0.05+200000*0.03+400000*0.015+(I-1000000)*0.01);
    printf("\n");
    return 0;
}
